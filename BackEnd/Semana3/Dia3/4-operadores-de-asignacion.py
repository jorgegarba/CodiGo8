# Operadores de Asignacion
# = Igual
# += Incremento
# -= Decremento
# *= Multiplicacion
# /= Division
# **= Potencia
num1, num2 = 20,5
num1=10
print(num1)
# num1 += num2 es lo mismo que haber puesto num1 = num1+num2
num1 += num2
print(num1)
# es lo mismo que haber puesto num1 = num1- num2
num1 -= num2
print(num1)
# es lo mismo que haber puesto num1 = num1 * num2
num1 *= num2
print(num1)
# es lo mismo que haber puesto num1 = num1 / num2
num1 /= num2
print(num1)
# es lo mismo que haber puesto num1 = num1 ** num2
num1 **= num2
print(num1)