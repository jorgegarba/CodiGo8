# Operadores Aritmeticos
# + suma
# - resta
# * multiplicacion
# / division
# % modulo(residuo)
# ** exponente
# // cociente
num1=10
num2=20
num3 = num1 + num2
print(num3)
num3 = num2 - num1
print(num3)
num3 = num2 * num1
print(num3)
num3 = num2 % num1
print(num3)
num3 = num2 ** 2
print(num3)
num3 = num2 // num1
print(num3)